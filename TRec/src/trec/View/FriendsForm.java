  private void send_request_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_send_request_buttonMouseClicked
    // TODO add your handling code here:
    String username = insert_username_field.getText();
    try {
      User user = UserController.getInstance().getUserByUsername(username);
      if(user != null) {
        UserController.getInstance().sendFriendRequest(current_user.getID(), user.getID());
        JOptionPane.showMessageDialog(null, "Friend request sent to user: " + username + " .", "Request sent!", JOptionPane.INFORMATION_MESSAGE);
        refresh();
      } else {
        JOptionPane.showMessageDialog(null, "User with username: " + username + " does not exist.", "User does not exist.", JOptionPane.ERROR_MESSAGE);
      }
    } catch (Exception e) {
      System.out.println(e.getMessage());
    }
  }//GEN-LAST:event_send_request_buttonMouseClicked

