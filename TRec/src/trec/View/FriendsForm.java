  private void delete_friend_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_delete_friend_buttonMouseClicked
    // TODO add your handling code here:
    try {
      if(friends_id.isEmpty()) {
        JOptionPane.showMessageDialog(null, "You have no friends!", "Who are you deleting?", JOptionPane.ERROR_MESSAGE);
      } else {
        User target_user = UserController.getInstance().getUserByUsername(friends_combo_box.getSelectedItem().toString());
        UserController.getInstance().deleteFriendRelationship(current_user.getID(), target_user.getID());
        JOptionPane.showMessageDialog(null, "Friend deleted!", "Success!", JOptionPane.INFORMATION_MESSAGE);
        refresh();
      }
    } catch (Exception e) {
      System.out.println(e.getMessage());
    }
  }//GEN-LAST:event_delete_friend_buttonMouseClicked

