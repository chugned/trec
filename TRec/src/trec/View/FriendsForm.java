  public FriendsForm() {
    initComponents();
    try {
      current_user = UserController.getInstance().getCurrentUser();
      home_menu_bar_adminhub.setVisible(current_user.isAdmin());
      System.out.println("get friends");
      friends_id = UserController.getInstance().getFriends(UserController.getInstance().getCurrentUser().getID());
      System.out.println("get requests");
      ArrayList<Integer> requests = UserController.getInstance().getAllFriendRequests(current_user.getID());
      System.out.println("requests size " + friends_id.size());
      number_of_requests_label.setText(String.valueOf(requests.size()));
      if(friends_id.isEmpty()) {
        name_label.setText("--");
        age_label.setText("--");
        email_label.setText("--");
        occupation_label.setText("--");
      } else {
        for(int id : friends_id) {
          friends_combo_box.addItem(UserController.getInstance().getUserByUserID(id).getUsername());
        }
        User first_user = UserController.getInstance().getUserByUserID(friends_id.get(0));
        name_label.setText(first_user.getFirstName() + " " + first_user.getLastName());
        age_label.setText(String.valueOf(first_user.getAge()));
        email_label.setText(first_user.getEmail());
        occupation_label.setText(first_user.getOccupation());
      }
    } catch (Exception e) {
      System.out.println(e.getMessage());
    }
  }
  
