  public FriendRequestsForm() {
    initComponents();
    current_user = UserController.getInstance().getCurrentUser();
    home_menu_bar_adminhub.setVisible(current_user.isAdmin());
    try {
      ArrayList<Integer> request_ids = UserController.getInstance().getAllFriendRequests(current_user.getID());
      if(request_ids.isEmpty()) {
        name_label.setText("--");
        age_label.setText("--");
        email_label.setText("--");
        occupation_label.setText("--");
      } else {
        for(int user_id : request_ids) {
          User user = UserController.getInstance().getUserByUserID(user_id);
          friends_combo_box.addItem(user.getUsername());
        }
        User first_user = UserController.getInstance().getUserByUserID(request_ids.get(0));
        name_label.setText(first_user.getFirstName() + " " + first_user.getLastName());
        age_label.setText(String.valueOf(first_user.getAge()));
        email_label.setText(first_user.getEmail());
        occupation_label.setText(first_user.getOccupation());
      }
    } catch(Exception e) {
      System.out.println(e.getMessage());
    }
  }
  
