  private void accept_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_accept_buttonMouseClicked
    // TODO add your handling code here:
    try {
      String username = friends_combo_box.getSelectedItem().toString();
      User user = UserController.getInstance().getUserByUsername(username);
      if (UserController.getInstance().acceptFriendRequest(user.getID(), current_user.getID())) {
        // success
        JOptionPane.showMessageDialog(null, "Friend added!", "Success!", JOptionPane.INFORMATION_MESSAGE);
        refresh();
      } else {
        JOptionPane.showMessageDialog(null, "somehow you are already friends!", "Ooops!", JOptionPane.ERROR_MESSAGE);
      }
    } catch (Exception e) {
      System.out.println(e.getMessage());
    }
  }//GEN-LAST:event_accept_buttonMouseClicked

  private void reject_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_reject_buttonMouseClicked
    // TODO add your handling code here:
    try {
      String username = friends_combo_box.getSelectedItem().toString();
      User user = UserController.getInstance().getUserByUsername(username);
      UserController.getInstance().deleteFriendRequest(user.getID(), current_user.getID());
      JOptionPane.showMessageDialog(null, "Friend request rejected!", "Success!", JOptionPane.INFORMATION_MESSAGE);
      refresh();
    } catch (Exception e) {
      System.out.println(e.getMessage());
    }
  }//GEN-LAST:event_reject_buttonMouseClicked

