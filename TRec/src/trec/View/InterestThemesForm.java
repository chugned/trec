  private void save_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_save_buttonMouseClicked
    // TODO add your handling code here:
    try {
      UserController.getInstance().deleteAllUserITs(UserController.getInstance().getCurrentUser().getID());
      for(JCheckBox ch : checkboxes) {
        if (ch.isSelected()) {
          int it_id = UserController.getInstance().getITIDByName(ch.getText());
          UserController.getInstance().addNewITPair(UserController.getInstance().getCurrentUser().getID(), it_id);
        }
      }
      JOptionPane.showMessageDialog(null, "Interest themes saved!", "Success!", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
      System.out.println(e.getMessage());
    }
  }//GEN-LAST:event_save_buttonMouseClicked

