  public InterestThemesForm() {
    initComponents();
    User current_user = UserController.getInstance().getCurrentUser();
    home_menu_bar_adminhub.setVisible(current_user.isAdmin());
    try {
      interest_themes_list = UserController.getInstance().getAllITs();
      jPanel2.setLayout(new GridLayout(0, 2, 10, 10));
      for (String it : interest_themes_list) {
        JCheckBox cb = new JCheckBox(it);
        checkboxes.add(cb);
        jPanel2.add(cb);
        jPanel2.revalidate();
        jPanel2.repaint();
      }
      ArrayList<Integer> checked_by_user = UserController.getInstance().getAllITbyUserID(current_user.getID());
      for(int it_id : checked_by_user) {
        for(JCheckBox ch : checkboxes) {
          String it_name = UserController.getInstance().getITNameByID(it_id);
          if(it_name.equals(ch.getText())) {
            ch.setSelected(true);
          }
        }
      }
      
    } catch (Exception e) {
      System.out.println(e.getMessage());
    }
  }

